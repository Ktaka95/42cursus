#include <stddef.h>

void	ft_bzero(void *s, size_t n)
{
	int	i;
	char	*voidtonull;

	i = 0;
	voidtonull = s;
	while (i < n)
	{
		*voidtonull = '\0';
		s++;
		i++;
	}
}
