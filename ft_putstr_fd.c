#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
}

int main(void)
{
	char	*s = NULL;
	ft_putstr_fd(s, 1);
	return (0);
}
